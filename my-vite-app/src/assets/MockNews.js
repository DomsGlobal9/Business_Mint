// Helper function to get unique years
export const getUniqueNewsYears = () => {
  const years = mockNews.map(news => news.year);
  return [...new Set(years)].sort((a, b) => b - a);
};

// Helper function to filter news by year
export const getNewsByYear = (year) => {
  if (year === null) return mockNews;
  return mockNews.filter(news => news.year === year);
};

// Helper function to get unique categories
export const getUniqueCategories = () => {
  const categories = mockNews.map(news => news.category);
  return [...new Set(categories)].sort();
};

// Helper function to filter news by category
export const getNewsByCategory = (category) => {
  if (category === null) return mockNews;
  return mockNews.filter(news => news.category === category);
};