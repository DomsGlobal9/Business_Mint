// Helper function to get unique years
export const getUniqueYears = () => {
  const years = mockAwards.map(award => award.year);
  return [...new Set(years)].sort((a, b) => b - a);
};

// Helper function to filter awards by year
export const getAwardsByYear = (year) => {
  if (year === null) return mockAwards;
  return mockAwards.filter(award => award.year === year);
};